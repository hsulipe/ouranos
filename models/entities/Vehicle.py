class Vehicle:
    def __init__(
        self, 
        type, 
        plate, 
        model, 
        year, 
        num_passengers, 
        drivers_document
    ):
        self.type = type
        self.plate = plate
        self.model = model
        self.year = year
        self.num_passengers = num_passengers
        self.drivers_document = drivers_document